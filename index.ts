// 型の練習をします
//「プリミティブ型」
// string型
const myName: string = "Koki";
console.log(myName);

// number型
const myNum: number = 123;
console.log(myNum);

// bigint型 (基本はnumber型で事足りるようだ)
const bigNum: bigint = 10n;
console.log(bigNum);

// boolean型
const bool: boolean = true;
console.log(bool);

// null型
const nullKata: null = null;
console.log(nullKata);

// undefined型
const unde: undefined = undefined;
console.log(unde);
