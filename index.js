// 型の練習をします
//「プリミティブ型」
// string型
var myName = "Koki";
console.log(myName);
// number型
var myNum = 123;
console.log(myNum);
// bigint型 (基本はnumber型で事足りるようだ)
var bigNum = 10n;
console.log(bigNum);
// boolean型
var bool = true;
console.log(bool);
// null型
var nullKata = null;
console.log(nullKata);
// undefined型
var unde = undefined;
console.log(unde);
